#Tuple with single elemtent 
a = (1)
b = (1,)
print(type(a))
print(type(b))

#Tuple with double elemtent 
a = (1,2)
print(type(a))

#Tuple with empty elemtent #considers empty element as tuple type 
a=()
print(type(a))

#String tuple type 
a = ("Hello") #string type
b = ("Hello",) #with comma becomes tuple type 
print(type(a))
print(type(b))

